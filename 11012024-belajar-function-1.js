function luasPersegi(sisi) {
    return sisi * sisi;
}

console.log(luasPersegi(4))
console.log(luasPersegi(8))
console.log(luasPersegi(9))

// Buat function yang melakukan rumus luas PersegiPanjang 
// dengan 2 parameter nilai yaitu sisi L = Panjang * Lebar

function luasPersegiPanjang(panjang, lebar) {
    return panjang * lebar
}

console.log(luasPersegiPanjang(2, 4))
console.log(luasPersegiPanjang(3, 8))
console.log(luasPersegiPanjang(5, 3))